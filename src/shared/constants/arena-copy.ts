export const TOP_RANK_LIMIT = 10;

export function rankTitle(rank: number): string | null {
  if (rank === 1) return ARENA_COPY.champion;
  if (rank === 2) return ARENA_COPY.silver;
  if (rank === 3) return ARENA_COPY.bronze;
  return null;
}

export function formatRankLabel(rank: number, tied: boolean): string {
  return tied ? `#${rank}=` : `#${rank}`;
}

export function duelStatusLabel(status: string): string {
  switch (status) {
    case 'open':
      return 'Aguardando heróis';
    case 'ready':
      return 'Prontos para combate';
    case 'completed':
      return 'Selado';
    default:
      return status;
  }
}

export function duelTypeLabel(isClassified: boolean): string {
  return isClassified ? ARENA_COPY.classifiedDuel : ARENA_COPY.friendlyDuel;
}

export function verdictLabel(
  outcome: 'player_a' | 'player_b' | 'draw',
  nameA: string,
  nameB: string,
): string {
  if (outcome === 'draw') return 'Empate sangrento';
  if (outcome === 'player_a') return `${nameA} prevaleceu`;
  return `${nameB} prevaleceu`;
}
